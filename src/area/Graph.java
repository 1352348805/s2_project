package area;

public abstract class Graph {

    private int high;
    private int wide;

    public int getHigh() {
        return high;
    }

    public void setHigh(int high) {
        this.high = high;
    }

    public int getWide() {
        return wide;
    }

    public void setWide(int wide) {
        this.wide = wide;
    }

    public abstract double GraphArea();
}
